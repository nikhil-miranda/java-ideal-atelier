package DesignPatterns._0_SOLID_Principles;

public class _2_OpenClosePrinciple {

    /**
     * Definition: Software entities (classes, modules, functions, etc.) should be open for extension,
     * but closed for modification.
     *
     * Open for extension: You should be able to extend the behavior of a class without editing its source code.
     * Closed for modification: You should not be able to modify the source code of a class to change its behavior.
     *
     * This is achieved by using interfaces and abstract classes.
     *
     *  Example:
     *     - You have a class called Danger.java which has a title and a message.
     *     - You want to add a serial number to it.
     *     - You can extend the Danger class and add the serial number to it.
     *     - This way, you don't have to modify the Danger class.
     */

}
